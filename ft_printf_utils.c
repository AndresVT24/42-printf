#include "ft_printf.h"

static int	ft_puthex_ptr(unsigned long n)
{
	int		count;
	char	*base;

	count = 0;
	base = "0123456789abcdef";
	if (n >= 16)
		count += ft_puthex_ptr(n / 16);
	count += ft_putchar_sum(base[n % 16]);
	return (count);
}